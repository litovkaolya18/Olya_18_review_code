/*
Хромой король.На квадратной доске расставлены монеты, достоинством от
1 до 100. Хромой король, находящийся в правом нижнем углу, мечтает
попасть в левый верхний. При этом он может передвигаться только в
клетку слева или сверху и хочет, чтобы сумма всех монет, оказавшихся
у него на пути, была бы максимальной.Определить эту сумму и путь,
каким образом должен двигаться король, чтобы ее собрать.Ввод и вывод
организовать при помощи текстовых файлов.Формат входных данных : в
первой строке входного файла записано число N - размер доски(1 < N < 80).
Далее следует N строк, каждая из которых содержит N целых чисел, представляющих
доску.В первую строку выходного файл нужно вывести единственное число:
максимальную сумму, а во второй строке вывести путь в виде строки символов,
обозначив символом L движение влево, а символом U - движение вверх.
*/

#include "Header.h"
#include <iostream>
#include <vector>
#include <fstream>
#include <string>
//FIX_ME: добавлена библиотека Windows API для установки кодировки консоли
#include <windows.h>
//FIX_ME: убран using namespace std; по Google Style
// using namespace std;


int main() {
    setlocale(0, "");

    std::fstream f1("f1.txt");
    if (!f1.is_open()) {
        std::cout << "Не удалось открыть файл f1.txt(" << std::endl;
        return 1;
    }

    std::fstream f2("f2.txt");
    if (!f2.is_open()) {
        std::cout << "Не удалось открыть файл f2.txt(" << std::endl;
        return 1;
    }

    //FIX_ME: Добавлена инициализация, проверка чтения и диапазона n
    //int n;
    //f1 >> n;
    int n = 0;
    if (!(f1 >> n)) {
        std::cout << "Ошибка чтения размера доски N." << std::endl;
        return 1;
    }
    if (n <= 1 || n >= 80) {
        std::cout << "Некорректный размер доски N (должно быть 1 < N < 80)."
            << std::endl;
        return 1;
    }

    std::vector<std::vector<int>> board(n, std::vector<int>(n, 0));
    for (int row = 0; row < n; ++row) {
        for (int col = 0; col < n; ++col) {
            //FIX_ME: добавлена проверка чтения
            //f1 >> board[row][col];
            if (!(f1 >> board[row][col])) {
                std::cout << "Ошибка чтения элемента доски ["
                    << row << "][" << col << "]." << std::endl;
                return 1;
            }
            //FIX_ME: можно добавить проверку диапазона монет (1..100).
            if (board[row][col] < 1 || board[row][col] > 100) {
                std::cout << "Некорректное значение монеты в ["
                    << row << "][" << col
                    << "], ожидается 1..100." << std::endl;
                return 1;
            }
        }
    }

    std::vector<std::vector<int>> dp(n, std::vector<int>(n, 0));

    solve(board, dp, n);

    f2 << dp[0][0] << std::endl;
    f2 << build_path(dp, n);

    f1.close();
    f2.close();

    return 0;
}
